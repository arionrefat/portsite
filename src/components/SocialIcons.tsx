interface SocialIconProps {
  platform: string;
  href: string;
  className?: string;
}

export const SocialIcon: React.FC<SocialIconProps> = ({ platform, href, className = "" }) => {
  const getIcon = () => {
    const normalizedPlatform = platform.toLowerCase();
    const normalizedHref = href.toLowerCase();
    
    if (normalizedPlatform.includes('github')) {
      return <GitHubIcon className="w-5 h-5" />;
    }
    if (normalizedPlatform.includes('linkedin')) {
      return <LinkedInIcon className="w-5 h-5" />;
    }
    if (normalizedPlatform.includes('x') || normalizedPlatform.includes('twitter')) {
      return <XIcon className="w-5 h-5" />;
    }
    if (normalizedPlatform.includes('google')) {
      return <GoogleIcon className="w-5 h-5" />;
    }
    if (normalizedPlatform.includes('email') || normalizedHref.includes('mailto:')) {
      return <EmailIcon className="w-5 h-5" />;
    }
    
    // Fallback to first letter if no icon matches
    return <span className="text-xs font-semibold">{platform.charAt(0)}</span>;
  };

  return (
    <a 
      href={href} 
      className={`hover:text-[var(--accent)] transition-colors ${className}`}
      title={platform}
      target="_blank"
      rel="noopener noreferrer"
    >
      {getIcon()}
    </a>
  );
};
